# mathmatical operations in numpy

import numpy as np
arr_1 = np.array([1, 2, 3, 4, 5])

print(arr_1 + 5)  # Output: [6 7 8 9 10]
print(arr_1 - 2)  # Output: [-1 0 1 2 3]
print(arr_1 * 3)  # Output: [ 3  6  9 12 15]
print(arr_1 / 2)  # Output: [0.5 1. 1.5 2.  2.5]
print(arr_1 ** 2)  # Output: [ 1  4  9 16 25]